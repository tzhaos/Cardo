export type WebNextLocale = keyof typeof WEB_NEXT_MESSAGES;
export type WebNextMessageKey = keyof (typeof WEB_NEXT_MESSAGES)['en'];

export function translateWebNext(
  locale: WebNextLocale,
  key: WebNextMessageKey,
  params?: Record<string, number | string>,
) {
  const template = WEB_NEXT_MESSAGES[locale][key] ?? WEB_NEXT_MESSAGES.en[key];
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (match, token: string) => {
    const value = params[token];
    return value === undefined ? match : String(value);
  });
}
