function packBase(
  partial: Pick<ThemePack, 'id' | 'name' | 'description'> & {
    colors: ThemePack['tokens']['colors'];
    elevation?: ThemePack['tokens']['elevation'];
    fonts?: FontTokenMap;
    radii?: RadiusTokenMap;
    chrome?: ThemePack['tokens']['chrome'];
    motion?: ThemePack['tokens']['motion'];
  },
): ThemePack {
  return {
    id: partial.id,
    version: '1.0.0',
    name: partial.name,
    description: partial.description,
    layoutProfileId: 'classic',
    options: OFFICIAL_THEME_OPTIONS,
    tokens: {
      colors: partial.colors,
      fonts: partial.fonts ?? DEFAULT_FONT_TOKENS,
      radii: partial.radii ?? DEFAULT_RADIUS_TOKENS,
      space: DEFAULT_SPACE_TOKENS,
      elevation: partial.elevation ?? {
        light: DEFAULT_ELEVATION_LIGHT,
        dark: DEFAULT_ELEVATION_DARK,
      },
      motion: partial.motion ?? DEFAULT_MOTION_TOKENS,
      chrome: partial.chrome ?? {
        blur: '18px',
        topbarOffset: '12px',
      },
    },
  };
}

